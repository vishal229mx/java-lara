//28. Develop a program to print prime numbers?
//    In the reverse order from 70 to 20...
class  Q28
{
	public static void main(String[] args) 
	{
		outer:
		for(int i = 70; i>=20  ; i-- )
		{
			for (int j = 2; j <= i/2 ; j++ )
		    {
				if (i%j == 0)
				{
					continue outer;
				}
		    }
			System.out.print(i + ",");
		}
		
	}
}
